package com.mika.beans;

public class Article {
	
	private String designation;
	private String fournisseur;
	private String referenceFournisseur;
	private String referenceInterne;
	private String matiere;
	private String couleur;
	private String lot;
	private String prixHT;
	private String hauteur;
	private String longueur;
	private String largeur;
	private String diametre;
	
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getFournisseur() {
		return fournisseur;
	}
	public void setFournisseur(String fournisseur) {
		this.fournisseur = fournisseur;
	}
	public String getReferenceFournisseur() {
		return referenceFournisseur;
	}
	public void setReferenceFournisseur(String referenceFournisseur) {
		this.referenceFournisseur = referenceFournisseur;
	}
	public String getReferenceInterne() {
		return referenceInterne;
	}
	public void setReferenceInterne(String referenceInterne) {
		this.referenceInterne = referenceInterne;
	}
	public String getMatiere() {
		return matiere;
	}
	public void setMatiere(String matiere) {
		this.matiere = matiere;
	}
	public String getCouleur() {
		return couleur;
	}
	public void setCouleur(String couleur) {
		this.couleur = couleur;
	}
	public String getLot() {
		return lot;
	}
	public void setLot(String lot) {
		this.lot = lot;
	}
	public String getPrixHT() {
		return prixHT;
	}
	public void setPrixHT(String prixHT) {
		this.prixHT = prixHT;
	}
	public String getHauteur() {
		return hauteur;
	}
	public void setHauteur(String hauteur) {
		this.hauteur = hauteur;
	}
	public String getLongueur() {
		return longueur;
	}
	public void setLongueur(String longueur) {
		this.longueur = longueur;
	}
	public String getLargeur() {
		return largeur;
	}
	public void setLargeur(String largeur) {
		this.largeur = largeur;
	}
	public String getDiametre() {
		return diametre;
	}
	public void setDiametre(String diametre) {
		this.diametre = diametre;
	}
}
